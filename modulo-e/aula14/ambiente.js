// com teste no início (while):
var a = 1;

while (a <= 6) {
  console.log(`while ${a}`);
  a++;
}

// com teste no final (do while):
var b = 1;

do {
  console.log(`do while ${b}`);
  b++;
} while (b <= 6);

// com controle lógico (for):
console.log('Vai começar...');

for (var c = 1; c <= 6; c++) {
  console.log(`for ${c}`);
}

console.log('Fim!');
